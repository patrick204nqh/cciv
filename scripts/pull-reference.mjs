#!/usr/bin/env node
import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const REFS_DIR = join(ROOT, '.cache', 'references');
const API_BASE = 'https://api.polyhaven.com';
const MODEL_CDN = 'https://dl.polyhaven.org/file/ph-assets/Models/gltf';

const COMP_TYPE = {
  5120: Int8Array, 5121: Uint8Array, 5122: Int16Array, 5123: Uint16Array,
  5124: Int32Array, 5125: Uint32Array, 5126: Float32Array,
};
const COMP_SIZE = { 5120: 1, 5121: 1, 5122: 2, 5123: 2, 5124: 4, 5125: 4, 5126: 4 };
const NUM_COMP = { SCALAR: 1, VEC2: 2, VEC3: 3, VEC4: 4 };

async function fetchJson(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`GET ${url} → ${res.status}`);
  return res.json();
}

async function downloadTo(url, destPath) {
  if (existsSync(destPath)) return false;
  const dir = dirname(destPath);
  if (!existsSync(dir)) mkdirSync(dir, { recursive: true });
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Download ${url} → ${res.status}`);
  writeFileSync(destPath, Buffer.from(await res.arrayBuffer()));
  return true;
}

function fmtFloat32(arr) {
  const parts = [];
  for (let i = 0; i < arr.length; i++) {
    if (i > 0 && i % 8 === 0) parts.push('\n  ');
    parts.push(arr[i].toFixed(6));
    if (i < arr.length - 1) parts.push(', ');
  }
  return parts.join('');
}

function fmtUint16(arr) {
  const parts = [];
  for (let i = 0; i < arr.length; i++) {
    if (i > 0 && i % 16 === 0) parts.push('\n  ');
    parts.push(String(arr[i]));
    if (i < arr.length - 1) parts.push(', ');
  }
  return parts.join('');
}

function fmtUint32(arr) {
  const parts = [];
  for (let i = 0; i < arr.length; i++) {
    if (i > 0 && i % 12 === 0) parts.push('\n  ');
    parts.push(String(arr[i]));
    if (i < arr.length - 1) parts.push(', ');
  }
  return parts.join('');
}

function argName(attrKey) {
  const map = { POSITION: 'pos', NORMAL: 'nml', TEXCOORD_0: 'uv', TEXCOORD_1: 'uv2' };
  return map[attrKey] || attrKey.toLowerCase();
}

async function pullReference(name, ref) {
  const { id, resolution } = ref;
  const refDir = join(REFS_DIR, id);
  const dataDir = join(refDir, 'data');
  const texDir = join(refDir, 'textures');
  const binPath = join(refDir, `${id}.bin`);

  mkdirSync(dataDir, { recursive: true });
  mkdirSync(texDir, { recursive: true });

  // Download glTF + bin
  const gltfUrl = `${MODEL_CDN}/${resolution}/${id}/${id}_${resolution}.gltf`;
  const binUrl = `${MODEL_CDN}/${resolution}/${id}/${id}.bin`;
  const gltfPath = join('/tmp', `ph_${id}_${resolution}.gltf`);

  let gltf;
  try {
    const gltfNew = await downloadTo(gltfUrl, gltfPath);
    const binNew = await downloadTo(binUrl, binPath);
    if (gltfNew || binNew) console.log(`  Downloaded glTF`);
    else console.log(`  glTF cached`);
    gltf = JSON.parse(readFileSync(gltfPath, 'utf-8'));
  } catch (e) {
    console.error(`  Failed to download: ${e.message}`);
    return;
  }

  const bin = readFileSync(binPath);
  const meshNames = {};
  for (const node of gltf.nodes) {
    if (node.mesh !== undefined) {
      meshNames[node.mesh] = node.name || `mesh_${node.mesh}`;
    }
  }

  const indexTsLines = [
    `// Reference: ${id} (${ref.copyright || 'unknown license'})`,
    `// Auto-generated by scripts/pull-reference.mjs`,
    `export interface MeshData {`,
    `  pos: Float32Array;`,
    `  nml: Float32Array;`,
    `  uv: Float32Array;`,
    `  indices: Uint16Array | Uint32Array;`,
    `  hasUV2?: boolean;`,
    `  uv2?: Float32Array;`,
    `}`,
  ];

  let meshCount = 0;
  for (let mi = 0; mi < gltf.meshes.length; mi++) {
    const name = meshNames[mi];
    const prim = gltf.meshes[mi].primitives[0];
    const attrs = prim.attributes;
    const idxAcc = gltf.accessors[prim.indices];
    const fields = [];

    for (const [attrKey, accIdx] of Object.entries(attrs)) {
      const acc = gltf.accessors[accIdx];
      const bv = gltf.bufferViews[acc.bufferView];
      const TypedArray = COMP_TYPE[acc.componentType];
      const elemSize = COMP_SIZE[acc.componentType];
      const numComp = NUM_COMP[acc.type];
      const count = acc.count;
      const byteOffset = bv.byteOffset + (acc.byteOffset || 0);
      if (bin.byteOffset + byteOffset + count * numComp * elemSize > bin.buffer.byteLength) continue;
      const arr = new TypedArray(bin.buffer, bin.byteOffset + byteOffset, count * numComp);
      const aname = argName(attrKey);
      const arrName = `${name}_${aname}`;
      let formatter;
      if (TypedArray === Float32Array) formatter = fmtFloat32;
      else if (TypedArray === Uint16Array) formatter = fmtUint16;
      else if (TypedArray === Uint32Array) formatter = fmtUint32;
      else continue;
      writeFileSync(join(dataDir, `${arrName}.js`),
        `export const ${arrName} = new ${TypedArray.name}([\n  ${formatter(arr)}\n]);\n`);
      fields.push({ fieldName: aname, arrName });
    }

    const idxBv = gltf.bufferViews[idxAcc.bufferView];
    const idxTyped = COMP_TYPE[idxAcc.componentType];
    const idxElemSize = COMP_SIZE[idxAcc.componentType];
    const idxByteOffset = idxBv.byteOffset + (idxAcc.byteOffset || 0);
    const idxArr = new idxTyped(bin.buffer, bin.byteOffset + idxByteOffset, idxAcc.count);
    const idxName = `${name}_idx`;
    let idxFormatter;
    if (idxTyped === Uint16Array) idxFormatter = fmtUint16;
    else if (idxTyped === Uint32Array) idxFormatter = fmtUint32;
    writeFileSync(join(dataDir, `${idxName}.js`),
      `export const ${idxName} = new ${idxTyped.name}([\n  ${idxFormatter(idxArr)}\n]);\n`);

    for (const f of fields) indexTsLines.push(`import { ${f.arrName} } from './data/${f.arrName}.js';`);
    indexTsLines.push(`import { ${idxName} } from './data/${idxName}.js';`);
    indexTsLines.push(`export const ${name}: MeshData = {`);
    for (const f of fields) indexTsLines.push(`  ${f.fieldName}: ${f.arrName},`);
    indexTsLines.push(`  indices: ${idxName},`);
    if (fields.some(f => f.fieldName === 'uv2')) indexTsLines.push(`  hasUV2: true,`);
    indexTsLines.push(`};`);
    meshCount++;
  }

  writeFileSync(join(refDir, 'index.ts'), indexTsLines.join('\n'));
  console.log(`  Extracted ${meshCount} mesh groups → .cache/references/${id}/`);

  // Download textures
  let texCount = 0;
  try {
    const files = await fetchJson(`${API_BASE}/files/${id}`);
    const texEntries = Object.entries(files).filter(([_, v]) => v[resolution]);
    for (const [apiKey, resEntry] of texEntries) {
      const fmt = Object.keys(resEntry[resolution]).find(f => resEntry[resolution][f]?.url);
      if (!fmt) continue;
      const { url } = resEntry[resolution][fmt];
      const ext = fmt === 'jpg' ? 'jpg' : fmt;
      const group = apiKey.split('_')[0];
      const groupDir = join(texDir, group);
      const dest = join(groupDir, `${apiKey}_${resolution}.${ext}`);
      const ok = await downloadTo(url, dest);
      if (ok) { texCount++; console.log(`  Texture: ${group}/${apiKey}_${resolution}.${ext}`); }
    }
    console.log(`  Downloaded ${texCount} textures → .cache/references/${id}/textures/`);
  } catch (e) {
    console.warn(`  Texture fetch failed: ${e.message}`);
  }
}

async function main() {
  const refs = JSON.parse(readFileSync(join(__dirname, 'references.json'), 'utf-8'));
  console.log('Pulling references → .cache/references/\n');
  for (const [name, ref] of Object.entries(refs)) {
    console.log(`[${name}]`);
    await pullReference(name, ref);
  }
  console.log('\nDone. References are throwaway — not in any build pipeline.');
}

main().catch(err => { console.error(err); process.exit(1); });
