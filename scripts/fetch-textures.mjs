#!/usr/bin/env node
import { readFileSync, writeFileSync, mkdirSync, existsSync, createWriteStream } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const CONFIG_PATH = join(__dirname, 'textures.config.json');
const PUBLIC_DIR = join(ROOT, 'public', 'textures');
const SOURCES_PATH = join(ROOT, 'src', 'textures', 'sources.ts');
const API_BASE = 'https://api.polyhaven.com';

const MAP_NAME_MAP = {
  diff: 'Diffuse',
  nor_gl: 'nor_gl',
  rough: 'Rough',
  metal: 'Metal',
  ao: 'AO',
  disp: 'Displacement',
};

const config = JSON.parse(readFileSync(CONFIG_PATH, 'utf-8'));
const { resolution, format, textures } = config;

async function fetchJson(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`GET ${url} → ${res.status}`);
  return res.json();
}

async function downloadFile(url, destPath) {
  if (existsSync(destPath)) {
    const size = (await import('fs')).statSync(destPath).size;
    if (size > 0) return false;
  }
  const dir = dirname(destPath);
  if (!existsSync(dir)) mkdirSync(dir, { recursive: true });

  const res = await fetch(url);
  if (!res.ok) throw new Error(`Download ${url} → ${res.status}`);
  const reader = res.body.getReader();
  const writer = createWriteStream(destPath);
  const pump = async () => {
    while (true) {
      const { done, value } = await reader.read();
      if (done) { writer.close(); break; }
      writer.write(value);
    }
  };
  await pump();
  return true;
}

async function main() {
  const entries = [];
  let totalBytes = 0;
  let downloaded = 0;
  let skipped = 0;

  for (const [name, texCfg] of Object.entries(textures)) {
    const { asset, maps, wrap } = texCfg;
    if (!asset) { console.log(`\nSkipping ${name}: no asset field`); continue; }
    console.log(`\nFetching file manifest for ${asset}...`);
    const files = await fetchJson(`${API_BASE}/files/${asset}`);

    const assetDir = join(PUBLIC_DIR, asset);
    if (!existsSync(assetDir)) mkdirSync(assetDir, { recursive: true });

    const mapPaths = {};

    for (const map of maps) {
      const apiMapName = MAP_NAME_MAP[map];
      if (!apiMapName) { console.warn(`  Unknown map: ${map}`); continue; }
      const resEntry = files[apiMapName]?.[resolution];
      if (!resEntry) {
        console.warn(`  ${map} @ ${resolution} not available for ${asset}, skipping`);
        continue;
      }
      const fmtEntry = resEntry[format];
      if (!fmtEntry) {
        console.warn(`  ${map} @ ${resolution} ${format} not available for ${asset}`);
        continue;
      }
      const { url, size } = fmtEntry;
      const ext = format === 'jpg' ? 'jpg' : format;
      const filename = `${asset}_${map}_${resolution}.${ext}`;
      const destPath = join(assetDir, filename);

      console.log(`  ${map}: ${(size / 1024 / 1024).toFixed(1)}MB → public/textures/${asset}/${filename}`);

      const wasDownloaded = await downloadFile(url, destPath);
      if (wasDownloaded) { downloaded++; totalBytes += size; }
      else skipped++;

      const relPath = `/textures/${asset}/${filename}`;
      mapPaths[map] = relPath;
      mapPaths[`_${map}_size`] = size;
    }

    entries.push({ name, asset, maps: mapPaths, wrap });
  }

  generateSources(entries, resolution, format);
  console.log(`\nDone: ${downloaded} downloaded, ${skipped} cached, ${(totalBytes / 1024 / 1024).toFixed(1)}MB new`);
}

function generateSources(entries, res, fmt) {
  const ext = fmt === 'jpg' ? 'jpg' : fmt;
  const lines = [`// Auto-generated by scripts/fetch-textures.mjs — do not edit manually`, `// Resolution: ${res}, Format: ${fmt}`, ``, `export interface TextureSet {`, `  diff: string;`, `  nor_gl?: string;`, `  rough?: string;`, `  metal?: string;`, `  ao?: string;`, `  disp?: string;`, `  wrapS?: number;`, `  wrapT?: number;`, `}`, ``, `export const TEXTURES: Record<string, TextureSet> = {`];

  for (const { name, asset, maps, wrap } of entries) {
    const { repeatS = 1, repeatT = 1 } = wrap || {};
    const mapEntries = Object.entries(maps)
      .filter(([k]) => !k.startsWith('_'))
      .map(([k, v]) => `    ${k}: '${v}'`);
    const allEntries = [...mapEntries, `    wrapS: ${repeatS}`, `    wrapT: ${repeatT}`];
    lines.push(`  '${name}': {`);
    lines.push(allEntries.join(',\n'));
    lines.push(`  },`);
  }

  lines.push(`};`);
  lines.push(``);
  writeFileSync(SOURCES_PATH, lines.join('\n'));
  console.log(`\nGenerated ${SOURCES_PATH}`);
}

main().catch(err => { console.error(err); process.exit(1); });
